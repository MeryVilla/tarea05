function calcular() {
    const cantidad_horas = parseInt(document.getElementById('cantidad_horas').value);
    const categoria = document.getElementById('categoria').value;
    const anio_servicio = parseInt(document.getElementById('anio_servicio').value);

    let tarifa = 0;
    switch (categoria) {
        case 'A':
            tarifa = 40;
            break;
        case 'B':
            tarifa = 35;
            break;
        case 'C':
            tarifa = 30;
            break;
        default:
            break;
    }

    let porc_bonificacion = 0;
    if (anio_servicio >= 1 && anio_servicio <= 3) {
        porc_bonificacion = 0.15;
    } else if (anio_servicio >= 4 && anio_servicio <= 7) {
        porc_bonificacion = 0.20;
    } else if (anio_servicio >= 8 && anio_servicio <= 12) {
        porc_bonificacion = 0.30;
    } else if (anio_servicio >= 13) {
        porc_bonificacion = 0.35;
    }

    const sueldo = cantidad_horas * tarifa;
    const bonificacion = sueldo * porc_bonificacion;
    const sueldo_neto = sueldo + bonificacion;

    document.getElementById('resultado').innerHTML = `Resultado <br>` +
        `Sueldo básico => ` + sueldo + `<br>` +
        `Bonificación => ` + bonificacion + `<br>` +
        `Sueldo Neto => ` + sueldo_neto + `<br>`;

    return false;
}

function limpiar() {
    document.getElementById('formulario').reset();
    document.getElementById('resultado').innerHTML = '';
}
